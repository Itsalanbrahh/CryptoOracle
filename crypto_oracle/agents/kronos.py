"""Kronos — Financial OHLCV forecasting via shiyu-coder/Kronos foundation model.

Kronos (AAAI 2026) is the first open-source foundation model trained on data from
45+ global exchanges. It generates full OHLCV forecasts autoregressively.

Installation (no PyPI package — clone alongside this project):
    git clone https://github.com/shiyu-coder/Kronos.git vendor/kronos
    pip install -r vendor/kronos/requirements.txt
    KRONOS_REPO_PATH=vendor/kronos   # add to .env

Models (all from HuggingFace NeoQuasar org):
    NeoQuasar/Kronos-mini   —  4.1M params, 2048 ctx  (fastest)
    NeoQuasar/Kronos-small  — 24.7M params,  512 ctx  (default)
    NeoQuasar/Kronos-base   — 102.3M params, 512 ctx  (most accurate)

Falls back to pure-Python GBM Monte Carlo if Kronos is not available.
Model singleton is cached at module level — loads once per process.
"""

