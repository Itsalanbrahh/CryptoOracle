logger = get_logger(__name__)

_SYSTEM = """You are Kronos, a quantitative trading agent powered by a financial OHLCV
foundation model trained on 45+ global exchanges (AAAI 2026).

You receive a 7-day OHLCV forecast generated by sampling multiple paths from the model.
Key metrics:
- median_return_7d_pct: expected close-to-close return
- bullish_probability: fraction of sampled paths where close > current
- implied_range_pct: (max predicted high - min predicted low) / current — volatility proxy
- predicted_high_7d / predicted_low_7d: best/worst case bounds from sampled paths
- volume_trend: predicted volume relative to recent average

Respond ONLY in this exact format (no extra text):
SIGNAL: BULLISH|BEARISH|NEUTRAL
CONFIDENCE: 0.XX
SUMMARY: one or two sentences referencing specific forecast numbers
DATA_POINTS: point1 | point2 | point3"""


# ---------------------------------------------------------------------------
# Model singleton
# ---------------------------------------------------------------------------

_predictor = None
_predictor_loaded = False


def _get_device() -> str:
    try:
        import torch
        if torch.cuda.is_available():
            return "cuda"
        if torch.backends.mps.is_available():
            return "mps"
    except ImportError:
        pass
    return "cpu"


def _load_predictor():
    global _predictor, _predictor_loaded
    if _predictor_loaded:
        return _predictor

    repo_path = os.getenv("KRONOS_REPO_PATH", "vendor/kronos")
    if not os.path.isdir(repo_path):
        logger.warning(
            "Kronos repo not found at %s — using GBM fallback. "
            "Run: git clone https://github.com/shiyu-coder/Kronos.git %s",
            repo_path, repo_path,
        )
        _predictor_loaded = True
        return None

    if repo_path not in sys.path:
        sys.path.insert(0, repo_path)

    model_name = os.getenv("KRONOS_MODEL", "NeoQuasar/Kronos-small")
    tokenizer_name = os.getenv(
        "KRONOS_TOKENIZER",
        "NeoQuasar/Kronos-Tokenizer-2k"
        if "mini" in model_name
        else "NeoQuasar/Kronos-Tokenizer-base",
    )
    device = _get_device()

    try:
        from model import Kronos, KronosPredictor, KronosTokenizer  # type: ignore

        logger.info("Loading Kronos model %s on %s …", model_name, device)
        tokenizer = KronosTokenizer.from_pretrained(tokenizer_name)
        model = Kronos.from_pretrained(model_name)

        max_context = 2048 if "mini" in model_name else 512
        _predictor = KronosPredictor(
            model, tokenizer, device=device, max_context=max_context
        )
        logger.info("Kronos predictor loaded successfully (%s)", model_name)
    except Exception as exc:
        logger.warning(
            "Kronos load failed (%s) — using GBM Monte Carlo fallback", exc
        )
        _predictor = None

    _predictor_loaded = True
    return _predictor


